    public void loadBang(){
        try {
            DefaultTableModel tb = (DefaultTableModel)tblProduct.getModel();
            con=dBConnection.getConnection();
            String sql="select * from Product";
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            tb.setRowCount(0);
            Object object[] = new Object[4];
            while (rs.next()) {
                object[0]=rs.getString("idSP");
                object[1]=rs.getString("tenSP");
                object[2]=rs.getInt("soluong");
                object[3]=df.format(Double.parseDouble(rs.getString("gia")));
                tb.addRow(object);
                tblProduct.setModel(tb);
            }
            st.close();
            rs.close();
            con.close();
        } catch (Exception e) {
        }
    }

    private void btAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAddActionPerformed
        try {
            con = dBConnection.getConnection();
        } catch (ClassNotFoundException | SQLException ex) {
        }
        try {
            if(txtID.getText().equals("")|| txtTenSP.getText().equals("")|| txtSoLuong.getText().equals("")|| txtGia.getText().equals("")){
                JOptionPane.showMessageDialog(this, "Chưa nhập đủ dữ liệu");
            }else{
                StringBuffer sb = new StringBuffer();
                String sqlCheckPK = "select idSP from Product where idSP = '" +txtID.getText()+ "'";
                Statement st = con.createStatement();
                ResultSet rs = st.executeQuery(sqlCheckPK);
                if (rs.next()) {
                    sb.append("ID sản phẩm trùng!");
                }
                if(sb.length()>0){
                    JOptionPane.showMessageDialog(this, sb.toString());
                }else{
                    String sqlInsertProduct = "Insert into Product values ('" +txtID.getText()+ "','" +txtTenSP.getText()+ "','"
                            +txtSoLuong.getText()+ "','" +txtGia.getText()+"')";
                    st = con.createStatement();
                    int kq = st.executeUpdate(sqlInsertProduct);
                    if (kq >0) {
                        JOptionPane.showMessageDialog(this, "Đã thêm");
                        dltTF();
                    }
                    st.close();
                    rs.close();
                    con.close();
                }
            }
            
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btAddActionPerformed


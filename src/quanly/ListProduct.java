    private void btUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btUpdateActionPerformed
        try {
            con = dBConnection.getConnection();
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(ListProduct.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            if(txtTenSP.getText().equals("")|| txtSoLuong.getText().equals("")|| txtGia.getText().equals("")){
                JOptionPane.showMessageDialog(this, "Chưa nhập đủ dữ liệu");
            }else{
                Statement st = con.createStatement();
                String sqlUpdateProduct = "Update product set tenSP = '" +txtTenSP.getText()+ "', soluong= '"
                        +txtSoLuong.getText()+ "', gia ='" +txtGia.getText()+"' where idSP = '"+txtID.getText()+"'";
                st = con.createStatement();
                int kq = st.executeUpdate(sqlUpdateProduct);
                if (kq >= 0) {
                    JOptionPane.showMessageDialog(this, "Đã cập nhật");
                    dltTF();
                }
                st.close();
                
            }
            con.close();
            
        } catch (Exception e) {
        }    
        
    }//GEN-LAST:event_btUpdateActionPerformed

    private void btDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDeleteActionPerformed
        try {
            con = dBConnection.getConnection();
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(ListProduct.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        try {
            String sql = "delete Product where idSP = '" +txtID.getText()+ "'";
            Statement st = con.createStatement();
            
            int check = JOptionPane.showConfirmDialog(this,"Xoá 1 dòng dữ liệu","Thông báo",JOptionPane.YES_NO_OPTION);
            
            if(check == JOptionPane.YES_OPTION){
                st.executeUpdate(sql);
                dltTF();
                JOptionPane.showMessageDialog(this, "Đã xóa");
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btDeleteActionPerformed

